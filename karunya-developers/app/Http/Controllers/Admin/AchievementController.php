     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $our_Achievement = Achievement::find($id);
        return view('admin.homepage.edit-achievement',compact('our_Achievement'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $request->validate([
            'complete_project' => 'required|integer',
            'active_on_client' => 'required|integer',
            'experience_team' => 'required|integer',
            'winning_award' => 'required|integer',
        ]);

        $our_Achievement = Achievement::find($id);
        $our_Achievement->complete_project = $request->complete_project;
        $our_Achievement->active_on_client = $request->active_on_client;
        $our_Achievement->experience_team = $request->experience_team;
        $our_Achievement->winning_award = $request->winning_award;
        $our_Achievement->save();
        return redirect()->back()->with('success','Achievement update successfully');
    }

