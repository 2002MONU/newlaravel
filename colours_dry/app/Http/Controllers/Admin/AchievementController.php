    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $request->validate([
        'dry_complete' =>'required|string',
        'garments_circulation' =>'required|string',
        'satisfied_customer' =>'required|string',
        ]);

        $achievement = Achievement::find($id);
        $achievement->satisfied_customer = $request->satisfied_customer;
        $achievement->garments_circulation = $request->garments_circulation;
        $achievement->dry_complete = $request->dry_complete;
        $achievement->save();
        return redirect()->back()->with('success','Update Successfully');

    }

